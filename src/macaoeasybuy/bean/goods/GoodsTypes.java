package macaoeasybuy.bean.goods;

public class GoodsTypes {

	private int Oid;
	private String TypeId;
	private String TypeName;
	private String TypeParentId;
	private int TypeSex;
	public int getOid() {
		return Oid;
	}
	public void setOid(int oid) {
		Oid = oid;
	}
	public String getTypeId() {
		return TypeId;
	}
	public void setTypeId(String typeId) {
		TypeId = typeId;
	}
	public String getTypeName() {
		return TypeName;
	}
	public void setTypeName(String typeName) {
		TypeName = typeName;
	}
	public String getTypeParentId() {
		return TypeParentId;
	}
	public void setTypeParentId(String typeParentId) {
		TypeParentId = typeParentId;
	}
	public int getTypeSex() {
		return TypeSex;
	}
	public void setTypeSex(int typeSex) {
		TypeSex = typeSex;
	}
	
	
	
}
